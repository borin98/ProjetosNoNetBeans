    private void btbcalculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbcalculoActionPerformed
        
        int a = Integer.parseInt( txtA.getValue ( ).toString ( ) ) ;
        int b = Integer.parseInt ( txtB.getValue ( ).toString ( ) ) ;
        int c = Integer.parseInt ( txtC.getValue ( ).toString ( ) ) ;
        
        double raiz1 = 0, raiz2 = 0 ; ;
        
        double delta = ( b*b ) - ( 4*a*c ) ;
        lblDelta.setText ( String.format ( "%.2f", delta ) ) ;
        
        if ( delta >= 0 )
        {
          
          lblRaiz.setText ( "Ra√≠zes reais" ) ;  
            
          raiz1 = ( -b + Math.sqrt( delta )/ ( 2*a ) ) ;
          raiz2 = ( -b - Math.sqrt ( delta )/ ( 2*a ) ) ;
          
          lblX1.setText( String.format ( "%.2f", raiz1 ) );
          lblX2.setText ( String.format ( "%.2f", raiz2 ) ) ;
          
          panRaizes.setVisible ( true ) ;
          
        }
        
        else
        {
            
          lblRaiz.setText ( "Raizes imagin√°rias" ) ;  
         
          panRaizes.setVisible ( false ) ;
          
        }
        
        panResultado.setVisible ( true ) ;
        
    }//GEN-LAST:event_btbcalculoActionPerformed

